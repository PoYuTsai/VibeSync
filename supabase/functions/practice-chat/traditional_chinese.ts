export function toTraditionalChinese(value: string): string {
  let converted = value;
  for (const [from, to] of PHRASE_REPLACEMENTS) {
    converted = converted.split(from).join(to);
  }
  return [...converted]
    .map((char) => CHAR_REPLACEMENTS[char] ?? char)
    .join("");
}

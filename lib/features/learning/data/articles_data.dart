ArticlePracticeGuide buildArticlePracticeGuide(Article article) {
  final practiceCategory = _practiceCategoryForArticle(article);
  final template = _practiceTemplates[practiceCategory]!;

  return ArticlePracticeGuide(
    category: practiceCategory,
    oneLineTakeaway: article.subtitle,
    whenToUse: template.whenToUse,
    mistakeExample: template.mistakeExample,
    goodExample: template.goodExample,
    practiceTitle: template.practiceTitle,
    practicePrompt: template.practicePrompt,
    carryBackHint: template.carryBackHint,
  );
}

String _practiceCategoryForArticle(Article article) {
  switch (article.id) {
    case '1':
    case '7':
      return '開話題';
    case '2':
    case '8':
    case '9':
    case '10':
    case '11':
    case '14':
      return '延伸與共鳴';
    case '21':
      return '推進與邀約';
    case '13':
    case '22':
    case '24':
      return '判斷與邊界';
  }

  if (article.category == '幽默與調情') {
    return '性張力與曖昧';
  }
  if (article.category == '深度交流') {
    return '延伸與共鳴';
  }
  if (article.category == '非語言溝通') {
    return '穩住自己';
  }
  return '穩住自己';
}

